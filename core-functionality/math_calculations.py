def sum_range(values):
    """
    Вычисляет сумму значений в списке.

    Args:
        values (list): Список чисел.

    Returns:
        float: Сумма значений.
    """
    return sum(values)


def average_range(values):
    """
    Вычисляет среднее значение в списке.

    Args:
        values (list): Список чисел.

    Returns:
        float: Среднее значение.
    """
    return sum(values) / len(values)